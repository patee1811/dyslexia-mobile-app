const defaultMastery = {
  minAccuracy: 0.8,
  minAttempts: 2,
  reviewAfterDays: [1, 3, 7],
};

type CoreTaskInput = {
  listen: {
    promptText: string;
    options: string[];
    answer: string;
    instruction: string;
  };
  sound?: {
    soundLabel: string;
    options: string[];
    answer: string;
    instruction: string;
  };
  blend: Extract<LessonTask, { type: 'blend_syllable' }>;
  minimalPair: Extract<LessonTask, { type: 'tone_minimal_pair' }>;
  readWord: Extract<LessonTask, { type: 'read_word' }>;
  readSentence: Extract<LessonTask, { type: 'read_sentence' }>;
  comprehension: Extract<LessonTask, { type: 'comprehension' }>;
};

const createCoreTasks = (lessonId: string, input: CoreTaskInput): LessonTask[] => {
  const tasks: LessonTask[] = [
    {
      id: `${lessonId}-listen`,
      type: 'listen_choose',
      ...input.listen,
    },
  ];

  if (input.sound) {
    tasks.push({
      id: `${lessonId}-sound`,
      type: 'sound_to_letter',
      ...input.sound,
    });
  }

  tasks.push(
    {
      ...input.blend,
      id: `${lessonId}-blend`,
    },
    {
      ...input.minimalPair,
      id: `${lessonId}-pair`,
    },
    {
      ...input.readWord,
      id: `${lessonId}-word`,
    },
    {
      ...input.readSentence,
      id: `${lessonId}-sentence`,
    },
    {
      ...input.comprehension,
      id: `${lessonId}-comprehension`,
    },
  );

  return tasks;
};

